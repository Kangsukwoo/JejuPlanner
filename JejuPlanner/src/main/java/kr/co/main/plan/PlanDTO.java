package kr.co.main.plan;

public class PlanDTO {
	public String plan_no;
	public String plan_sub;
	public String plan_start;
	public String plan_end;
	public String plan_kind;
	public String plan_des;
	
	public PlanDTO() {
		
		}	
	


	public String getPlan_no() {
		return plan_no;
	}

	public void setPlan_no(String plan_no) {
		this.plan_no = plan_no;
	}

	public String getPlan_sub() {
		return plan_sub;
	}

	public void setPlan_sub(String plan_sub) {
		this.plan_sub = plan_sub;
	}

	public String getPlan_start() {
		return plan_start;
	}

	public void setPlan_start(String plan_start) {
		this.plan_start = plan_start;
	}

	public String getPlan_end() {
		return plan_end;
	}

	public void setPlan_end(String plan_end) {
		this.plan_end = plan_end;
	}

	public String getPlan_kind() {
		return plan_kind;
	}

	public void setPlan_kind(String plan_kind) {
		this.plan_kind = plan_kind;
	}

	public String getPlan_des() {
		return plan_des;
	}

	public void setPlan_des(String plan_des) {
		this.plan_des = plan_des;
	}
	
	
	
	
	
}//PlanDTO end
