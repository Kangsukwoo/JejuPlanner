package kr.co.main.plan;

public class Plan_timeDTO {
	private int pt_no;
	private String sc_code;
	private String pt_start;
	private String pt_end;
	private String contentid;
	
	public Plan_timeDTO() {
	}

	public int getPt_no() {
		return pt_no;
	}

	public void setPt_no(int pt_no) {
		this.pt_no = pt_no;
	}

	public String getSc_code() {
		return sc_code;
	}

	public void setSc_code(String sc_code) {
		this.sc_code = sc_code;
	}

	public String getPt_start() {
		return pt_start;
	}

	public void setPt_start(String pt_start) {
		this.pt_start = pt_start;
	}

	public String getPt_end() {
		return pt_end;
	}

	public void setPt_end(String pt_end) {
		this.pt_end = pt_end;
	}

	public String getContentid() {
		return contentid;
	}

	public void setContentid(String contentid) {
		this.contentid = contentid;
	}
	
	
	
	
	
}//Plan_timeDTO end
